package tp2.ejercicio09;

public class SumaDeEnteros {
    private Integer sum, dif;

    public SumaDeEnteros() {
        super();
    }

    public SumaDeEnteros(Integer sum, Integer dif) {
        super();
        this.sum = sum;
        this.dif = dif;
    }

    public Integer getSum() {
        return sum;
    }

    public void setSum(Integer sum) {
        this.sum = sum;
    }

    public Integer getDif() {
        return dif;
    }

    public void setDif(Integer dif) {
        this.dif = dif;
    }
}
